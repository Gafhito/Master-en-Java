package Seccion_2_Variables;

public class PrimitivosFloat {
    public static void main(String[] args) {
        float realFloat = 1.0f;
        System.out.println("realFloat = " + realFloat);

        // Notación científica
        float realExponente = 2.12e3f;  // 2120.0f
        System.out.println("realExponente = " + realExponente);

        float exponenteNegativo = 1.5e-10f;  // 0.00000000015f
        System.out.println("exponenteNegativo = " + exponenteNegativo);
    }
}
